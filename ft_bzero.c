#include "libft.h"

void ft_bzero(void *str, size_t i)
{
	size_t j;
	char *s;

	s = str;
	j = 0;
	while(s[j] || j < i)
	{
		if (j < i)
			s[j] = '\0';
		j++;
	}
}
